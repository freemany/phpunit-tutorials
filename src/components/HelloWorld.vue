<template>
  <div class="container">
    <div>
      <h5>Master</h5>
      <div class="jumbotron p-3 p-md-5 rounded ol-md-6 px-0" v-html="content" ref="slideContent">
      </div>
    </div>
    <button @click="prev">prev</button>{{index + 1}} of {{total}}<button @click="next">next</button>
  </div>
  </template>

<script>
import config from '@/config/slideshow';
import hljs from 'highlightjs';
import {getProgressPageIndex, setProgressPageIndex} from '@/lib/localstorage';


export default {
  name: 'HelloWorld',
  props: {
    msg: String
  },
  data() {
      return {
          total: 0,
          index: 0,
          content: null,
      }
  },
  created() {
      this.total = config.length;
      this.index = getProgressPageIndex();
      this.content = config[this.index].content;
  },
  mounted() {
    this.initCode();
  },
  methods: {
      initCode() {
          this.$refs.slideContent.querySelectorAll('pre code').forEach((block) => {
              hljs.highlightBlock(block);
          });
      },
      next() {
          if (this.index === this.total - 1) {
              return;
          }
          this.index ++;
          setProgressPageIndex(this.index);
          this.content = config[this.index].content;
          this.$nextTick(() => {
              this.initCode();
          });
      },
      prev() {
          if (this.index === 0) {
              return;
          }
          this.index --;
          setProgressPageIndex(this.index);
          this.content = config[this.index].content;
          this.$nextTick(() => {
              this.initCode();
          });
      },
  }
}
</script>

